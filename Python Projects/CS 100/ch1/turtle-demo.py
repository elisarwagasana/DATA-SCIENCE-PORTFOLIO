def drawRightTriangle():
    x=t.xcor()
    y=t.ycor()
    t.setheading(0)
    t.pendown()
    t.forward(90)
    t.right(90)
    t.forward(120)
    t.goto(x,y)
    
    t.penup()
    t.goto(-100,100)
    t.pendown()
    t.pencolor("magenta")
drawRightTriangle()

