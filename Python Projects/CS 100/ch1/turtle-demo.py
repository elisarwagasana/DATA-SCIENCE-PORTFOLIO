import turtle
t=turtle.Turtle()
print("orig.pos:", t.position() )

t.pencolor("red")
t.width(10)
t.forward(300)
t.penup()
t.left(90)
t.forward(50)
t.left(90)
t.pendown()
t.width(5)
t.pencolor("blue")
t.forward(300)

def drawThickGreenLine():
    t.penup()
    x=t.xcor()
    y=t.ycor()
    print(x,y)
    t.goto(x-365, y-352)
    t.pendown()
    t.pencolor("green")
    t.setheading(90)
    t.forward(615)
drawThickGreenLine()
t.right(90)
t.forward(727)
